        <h1 id="introduction">Introduction</h1>
<aside>
    <strong>Base URL</strong>: <code>http://localhost</code>
</aside>
<p>Documentação da API pra AiQFome. Uma vez que você execute o docker compose up, as migrations e seeders serão executadas automaticamente, disponibilizando 2 usuários necessários para testes.</p>
<p>Esta API tem 2 tipos de usuários:</p>
<ul>
<li><strong>Cliente (client)</strong>: permissões voltadas pra produtos favoritos e ver seu próprio registro  </li>
<li><strong>Admin</strong>: voltado para gerenciamento de clientes, porém não tem acesso à lista de produtos favoritos  </li>
</ul>
<p>Segue os usuários:</p>
<ul>
<li><strong>Admin:</strong> <code>admin@aiqfome.com.br</code> | <code>Me_contrata_ai@123</code>  </li>
<li><strong>Cliente:</strong> <code>client@aiqfome.com.br</code> | <code>Me_contrata_ai@123</code>  </li>
</ul>
<p>Outra coisa importante: você vai precisar do <strong>código JWT</strong> fornecido ao iniciar sessão para testar alguns endpoints.</p>
<hr />
<h3 id="sugestao-de-como-testar-a-api">Sugestão de como testar a API</h3>
<ol>
<li>Liste os produtos e o produto por ID — esses endpoints <strong>não necessitam de autenticação</strong>.<br />
Na segunda vez que essa requisição for feita, será mais rápida e menos custosa, pois será <strong>cacheada</strong>.</li>
<li>Faça login com o <strong>admin</strong> — gerencie os clientes, crie novos usuários, edite, delete, liste todos.</li>
<li>Faça login com o <strong>cliente</strong> — gerencie produtos favoritos.</li>
<li>Acesse o “cria nova sessão” e faça o login.</li>
</ol>
